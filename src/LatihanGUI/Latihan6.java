    private void buttonSamadenganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSamadenganActionPerformed
        // TODO add your handling code here:
        switch (pilih) {
  case 1:
      angka2 = Double.parseDouble(angka);
      jumlah = angka1 + angka2;
      angka = Double.toString(jumlah);
      display.setText(angka);
      break;
  case 2:
      angka2 = Double.parseDouble(angka);
      jumlah = angka1 - angka2;
      angka = Double.toString(jumlah);
      display.setText(angka);
      break;
   case 3:
      angka2 = Double.parseDouble(angka);
      jumlah = angka1 * angka2;
      angka = Double.toString(jumlah);
      display.setText(angka);
      break;
   case 4:
      angka2 = Double.parseDouble(angka);
      jumlah = angka1 / angka2;
      angka = Double.toString(jumlah);
      display.setText(angka);
      break;
   case 5:
       angka2 = Double.parseDouble(angka);
       jumlah = angka1*angka2/100;
       angka = Double.toString(jumlah);
       display.setText(angka); 
       break;
   case 6:
      jumlah = 1/angka1;
      angka = Double.toString(jumlah);
      display.setText(angka);
      break;
   case 7:
       angka2 =Double.parseDouble(angka);
       jumlah = Math.sqrt(angka1);
       angka = Double.toString(jumlah);
       display.setText(angka);
      break;
   case 8 :
       angka2 = Double.parseDouble(angka);
       jumlah = angka1 - angka2;
       angka = Double.toString(jumlah);
       display.setText("-"+angka);
       break;
      default:
      break;
 }
    }//GEN-LAST:event_buttonSamadenganActionPerformed

